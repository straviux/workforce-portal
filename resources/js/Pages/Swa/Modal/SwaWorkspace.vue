        resetReportEditState();
        clearDraftPreview();
        toast.add({
            severity: 'error',
            summary: 'Error',
            detail: error.response?.data?.message ?? 'Could not load the saved SWA for editing.',
            life: 4000,
        });
    } finally {
        isHydratingReport.value = false;
        loadingEditorReportId.value = null;
    }
}

function deleteSavedReport(report) {
