function editDraftValues() {
    if (!canEditDraftValues.value) return;

    const workDates = buildWorkDates();
    const sourceRows = draftSourceRows.value;
    const currentDraftRows = new Map(draftRows.value.map((row) => [row.sort_order, row]));

    draftDates.value = workDates;
    draftRows.value = sourceRows.map((task) => {
        const existingRow = currentDraftRows.get(task.sort_order);
        const shouldPreserveValues = existingRow?.task_type === task.task_type;
        const existingValues = new Map((shouldPreserveValues ? existingRow?.daily_values : []).map((value) => [value.work_date, value]));

        return {
            sort_order: task.sort_order,
            task_name: String(task.task_name ?? '').trim(),
            task_type: task.task_type,
            daily_values: workDates.map((workDate) => {
                const existingValue = existingValues.get(workDate);

                return {
                    work_date: workDate,
                    numeric_value: task.task_type === 'countable'
                        ? Number(existingValue?.numeric_value ?? 0)
                        : null,
                    mark_value: task.task_type === 'check_blank'
                        ? (existingValue?.mark_value === 'check' ? 'check' : 'dash')
                        : null,
                };
            }),
        };
    });
}

function rebuildDraftValues() {
    if (!canPrepareDraft.value) return;

    prepareDraftValues();
}

function buildWorkDates() {
    const start = dayjs(generation.period_start_date);
    const end = dayjs(generation.period_end_date);
    const selectedDays = new Set(generation.work_days);
    const dates = [];
    let cursor = start.startOf('day');

    while (!cursor.isAfter(end, 'day')) {
        const currentDate = cursor.format('YYYY-MM-DD');

        if (selectedDays.has(cursor.format('dddd').toLowerCase()) && !blockedDateSet.value.has(currentDate)) {
            dates.push(currentDate);
        }

        cursor = cursor.add(1, 'day');
    }

    return dates;
}

