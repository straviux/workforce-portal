function formatSwaPeriodLabel(startValue, endValue) {
    if (!startValue || !endValue) return '—';

    const start = dayjs(startValue);
    const end = dayjs(endValue);

    if (!start.isValid() || !end.isValid()) return '—';

    if (start.year() === end.year()) {
        if (start.month() === end.month()) {
            return `${start.format('MMMM').toUpperCase()} ${start.date()}-${end.date()}, ${end.format('YYYY')}`;
        }

        return `${start.format('MMMM').toUpperCase()} ${start.date()}-${end.format('MMMM').toUpperCase()} ${end.date()}, ${end.format('YYYY')}`;
    }

    return `${start.format('MMMM').toUpperCase()} ${start.date()}, ${start.format('YYYY')} - ${end.format('MMMM').toUpperCase()} ${end.date()}, ${end.format('YYYY')}`;
}

function formatUpperText(value) {
    const text = String(value ?? '').trim();
    return text ? text.toUpperCase() : '';
}

