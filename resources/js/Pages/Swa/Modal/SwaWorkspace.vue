function parseDateValue(value) {
    if (!value) return null;

    const parsed = dayjs(value);
    return parsed.isValid() ? parsed.toDate() : null;
}

function createTaskRow(sortOrder, persistedRange = {}) {
    return {
        sort_order: sortOrder,
        task_name: '',
        task_type: 'countable',
        auto_min: normalizeStoredRangeValue(persistedRange.min, DEFAULT_COUNTABLE_MIN),
        auto_max: normalizeStoredRangeValue(persistedRange.max, DEFAULT_COUNTABLE_MAX),
    };
}

function normalizeStoredRangeValue(value, fallback) {
    const numericValue = Number(value);

    if (!Number.isFinite(numericValue)) return fallback;

    return Math.max(0, Math.trunc(numericValue));
}

function loadCountableRangeSettings() {
    const storageKey = getCountableRangeStorageKey();

    if (!storageKey || typeof window === 'undefined') return {};

    try {
        return JSON.parse(window.localStorage.getItem(storageKey) ?? '{}');
    } catch {
        return {};
    }
}

function getBlockedEventsInRange(startValue, endValue) {
    if (!startValue || !endValue) return [];

    const start = dayjs(startValue).startOf('day');
    const end = dayjs(endValue).endOf('day');

    return (props.calendarEvents ?? [])
        .filter((event) => {
            const eventDate = dayjs(event.event_date);
            return eventDate.isValid() && !eventDate.isBefore(start, 'day') && !eventDate.isAfter(end, 'day');
        })
        .sort((left, right) => left.event_date.localeCompare(right.event_date));
}

function buildSwaDocumentRows(startValue, endValue, workDays, rows, blockedEvents = []) {
    if (!startValue || !endValue || !rows.length) return [];

    const selectedDays = new Set((workDays ?? []).map((day) => String(day).toLowerCase()));
    const blockedEventMap = new Map(blockedEvents.map((event) => [event.event_date, event]));
    const documentRows = [];
    let cursor = dayjs(startValue).startOf('day');
    const end = dayjs(endValue).startOf('day');

    while (!cursor.isAfter(end, 'day')) {
        const workDate = cursor.format('YYYY-MM-DD');
        const weekday = cursor.format('dddd').toLowerCase();
        const blockedEvent = blockedEventMap.get(workDate);

        if (blockedEvent) {
            documentRows.push({
                key: `special-${workDate}`,
                kind: blockedEvent.event_type === 'work_suspension' ? 'work_suspension' : 'special',
                day_number: cursor.date(),
                label: blockedEvent.event_type === 'work_suspension' ? 'WORK SUSPENSION' : 'HOLIDAY',
            });
        } else if (!selectedDays.has(weekday)) {
            documentRows.push({
                key: `special-${workDate}`,
                kind: 'offday',
                day_number: cursor.date(),
                label: cursor.format('dddd').toUpperCase(),
            });
        } else {
            documentRows.push({
                key: `work-${workDate}`,
                kind: 'work',
                day_number: cursor.date(),
                task_values: rows.map((task) => ({
                    sort_order: task.sort_order,
                    task_type: task.task_type,
                    cell: task.daily_values.find((value) => value.work_date === workDate) ?? {
                        work_date: workDate,
                        numeric_value: 0,
                        mark_value: 'dash',
                    },
                })),
            });
        }

        cursor = cursor.add(1, 'day');
    }

    return documentRows;
}

function persistCountableRangeSettings() {
    const storageKey = getCountableRangeStorageKey();

    if (!storageKey || typeof window === 'undefined') return;

    const payload = taskRows.value.reduce((carry, row) => {
        carry[row.sort_order] = {
            min: row.auto_min,
            max: row.auto_max,
        };

        return carry;
    }, {});

    window.localStorage.setItem(storageKey, JSON.stringify(payload));
}

function getCountableRangeStorageKey() {
    if (!props.subject) return null;

    const subjectKey = props.subject.id
        ?? props.subject.employee_id
        ?? props.subject.employee_no
        ?? props.subject.display_name
        ?? 'subject';

    return `swa:countable-range:${props.moduleType}:${subjectKey}`;
}

function savedReportEndpoint(reportId) {
    if (props.moduleType === 'personal') {
        return `/api/swa/personal/reports/${reportId}`;
    }

    return `/api/swa/employees/${props.subject?.id}/reports/${reportId}`;
}

function isCountableRangeInvalid(row) {
    if (row.task_type !== 'countable') return false;

    return !Number.isFinite(row.auto_min)
        || !Number.isFinite(row.auto_max)
        || row.auto_min < 0
        || row.auto_max < row.auto_min;
}

function resolveCountableRange(row) {
    const min = normalizeStoredRangeValue(row.auto_min, DEFAULT_COUNTABLE_MIN);
    const max = normalizeStoredRangeValue(row.auto_max, DEFAULT_COUNTABLE_MAX);

    return {
        min,
        max: Math.max(min, max),
    };
}

